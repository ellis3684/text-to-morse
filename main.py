user_text = input("What would you like to say in Morse code? ")
morse_output = ""

# Loops through the user's string input, and returns the Morse equivalent. If the user's input includes any input
# that is outside of the scope of the Morse dictionary I hardcoded, the loop ignores it and continues.
for char in user_text:
    if char in morse_dict:
        morse_output += morse_dict[char]
        morse_output += " "
    else:
        continue

# The Morse output is printed in the output below.
print(morse_output)
